using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace DungeonCrawl
{
    public class Monster
    {
        /// <summary>
        /// This is the health of the monster.
        /// </summary>
        public int HP;
        /// <summary>
        /// This will determine the base HP and Attack.
        /// </summary>
        public int Level;
        /// <summary>
        /// This is how much damage the monster does
        /// </summary>
        public int Attack;
    }
}